package java.lang;

public class Error extends Throwable {
	public Error() {
		super();
	}

	public Error(java.lang.String message, java.lang.Throwable cause) {
		super(message, cause);
	}

	public Error(java.lang.String message) {
		super(message);
	}

	public Error(java.lang.Throwable cause) {
		super(cause);
	}
}
